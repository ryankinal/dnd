let queryString = search && search.replace(/^\?/, '').split('&').map((item) => {
		if (typeof item !== 'string') {
			return true;
		}

		const parts = item.split('=');

		return {
			name: parts[0],
			value: parts.length > 1 ? parts[1] : null
		}
	}).reduce((output, input) => {
		output[input.name] = input.value;
		return output;
	}, {});

